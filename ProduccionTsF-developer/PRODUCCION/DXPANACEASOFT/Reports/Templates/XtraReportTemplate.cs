    private void XtraReportTemplate_BeforePrint(object sender, System.Drawing.Printing.PrintEventArgs e)
    {
        using (DBContext db = new DBContext())
        {
            int id = (int)this.id_company.Value;
            Company company = db.Company.FirstOrDefault(c => c.id == id);

            if (company != null)
            {
                byte[] data = company.logo;
                MemoryStream stream = new MemoryStream(data);
                xrPictureBoxLogo.Image = Image.FromStream(stream);

                xrTableCellBuisinessName.Text = company.businessName;
                xrTableCellRuc.Text = company.ruc;

                xrTableCellAddress.Text = company.address;
                xrTableCellPhone.Text = company.phoneNumber;
            }
        }
    }
